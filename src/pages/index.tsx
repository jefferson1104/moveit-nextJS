// Funcao para pegar informacoes dos cookies
/* 
  getServerSideProps: tudo que fazemos utilizando essa funcao, faz com que as informacoes carregads seja de uma api,
  de um cookie, banco de dados, localstorage e etc.. sejam carregadas pelo servidor node e nao pelo browser, tornando 
  possivel que os motores de buscas facam uma indexacao deste conteudo, e foi a partir desta funcao que o nextjs surgiu 
  e comecou a ter mais  notoriedade no mercado, resolvendo os problemas sobre SEO.
*/
export const getServerSideProps: GetServerSideProps = async (ctx) => {
  const { level, currentExperience, challengesCompleted } = ctx.req.cookies;

  return {
    props: {
      level: Number(level),
      currentExperience: Number(currentExperience),
      challengesCompleted: Number(challengesCompleted)
    }
  }
}